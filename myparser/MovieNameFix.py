def movie_name_fix(title):
    return title.replace("奴●", "奴隷")\
        .replace("レ●プ", "レイプ")\
        .replace("犯●れ", "犯され")\
        .replace("痴●", "痴漢")\
        .replace("催●", "催眠")\
        .replace("●肉", "幼肉")\
        .replace("強●", "強制")\
        .replace("陵●", "陵辱")\
        .replace("凌●", "凌辱")\
        .replace("虐●", "虐待")\
        .replace("輪●", "輪姦")\
        .replace("折●", "折檻")\
        .replace("ロ●ータ", "ロリータ")\
        .replace("●す", "犯す")
